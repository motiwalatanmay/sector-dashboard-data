const _clamp = (v, lo, hi) => Math.min(hi, Math.max(lo, v));
const _r1    = (v) => Math.round(v * 10) / 10;
const _fp    = (v) => `${v >= 0 ? "+" : ""}${(v * 100).toFixed(1)}%`;
const _fpe   = (v) => v != null ? `${v.toFixed(1)}x` : "—";

function _scoreVal(curPEorGap, medPE) {
  const gap = (medPE === undefined) ? curPEorGap : (curPEorGap && medPE ? (curPEorGap / medPE) - 1 : null);
  if (gap == null || isNaN(gap)) return { score:20, ratio:null, notes:[] };
  const score = _r1(_clamp(40 * (0.30 - gap) / 0.60, 0, 40));
  const pct   = Math.abs(gap * 100).toFixed(0);
  const dir   = gap < 0 ? "below" : "above";
  const lbl   = gap < -0.20 ? "cheap zone" : gap < 0 ? "below fairPE" : gap > 0.20 ? "expensive zone" : "near fairPE";
  return { score, ratio: gap + 1, notes: [`PE is ${pct}% ${dir} fair value (${lbl})`] };
}
function _scoreGrowth(c) {
  if (c == null) return { score:15, notes:["3Y CAGR unavailable"] };
  const score = _r1(_clamp(30 * (c + 0.10) / 0.30, 0, 30));
  const lbl   = c >= 0.20 ? "exceptional" : c >= 0.12 ? "healthy" : c >= 0.05 ? "moderate" : c >= 0 ? "weak" : "shrinking";
  return { score, notes: [`3Y CAGR ${_fp(c)} — ${lbl} earnings growth`] };
}
function _scoreTrend(et) {
  if (et == null) return { score:10, notes:["Earnings trend unavailable"] };
  const score = _r1(_clamp(20 * (et + 0.10) / 0.25, 0, 20));
  const lbl   = et >= 0.05 ? "growing YoY" : et <= -0.05 ? "declining YoY" : "flat YoY";
  return { score, notes: [`YoY earnings ${_fp(et)} — ${lbl}`] };
}
function _scoreVol(hi, lo, med) {
  if (!hi || !lo || !med) return { score:5, notes:[] };
  const vol   = (hi - lo) / med;
  const score = _r1(_clamp(10 * (1 - (vol - 0.5) / 1.5), 0, 10));
  const lbl   = vol < 0.8 ? "low — signal reliable" : vol < 1.4 ? "moderate" : "high — signal uncertain";
  return { score, notes: [`PE range ${_fpe(lo)}–${_fpe(hi)}: volatility ${lbl}`] };
}
function _scoreLabel(s) {
  return s >= 75 ? "Highly attractive" : s >= 60 ? "Attractive" : s >= 45 ? "Fairly valued" : s >= 30 ? "Stretched" : "Overvalued";
}
function getSignal(data) {
  const { marketPE, medianPE, cagr3, earnTrend, highPE, lowPE } = data ?? {};
  if (!marketPE || !medianPE)
    return { signal:"HOLD", score:50, scoreLabel:"Fairly valued", reasoning:["Insufficient data"] };
  const vGap = data?.valuationGap ?? (marketPE && medianPE ? (marketPE / medianPE) - 1 : null);
  const val  = _scoreVal(vGap);
  const grow = _scoreGrowth(cagr3);
  const trd  = _scoreTrend(earnTrend);
  const vol  = _scoreVol(highPE, lowPE, medianPE);
  const score = _clamp(Math.round(val.score + grow.score + trd.score + vol.score), 0, 100);
  const { ratio } = val;
  const signal = ratio < 0.85 && earnTrend > 0         ? "STRONG BUY"
               : ratio < 1.0  && (cagr3 ?? 0) >= 0.12 ? "BUY"
               : ratio > 1.15 && earnTrend < 0          ? "SELL"
               :                                          "HOLD";
  const ctx = [];
  if (signal === "HOLD" && score >= 70 && medianPE)
    ctx.push(`Watch entry: signal improves if PE falls below median ${_fpe(medianPE)}`);
  if (signal === "SELL" && score >= 55 && (cagr3 ?? 0) >= 0.12)
    ctx.push(`Prefer partial trim — long-run CAGR ${_fp(cagr3)} still intact`);
  if (signal === "BUY"  && score < 55)
    ctx.push("Low-conviction BUY: entry conditions marginally met — size position small");
  return { signal, score, scoreLabel:_scoreLabel(score),
           reasoning:[...val.notes, ...grow.notes, ...trd.notes, ...vol.notes, ...ctx] };
}

