// Converts a "YYYY-MM" date string → Indian FY quarter label e.g. "Q2 FY26"
// FY mapping: Apr–Jun = Q1, Jul–Sep = Q2, Oct–Dec = Q3, Jan–Mar = Q4
// FY year: if month >= 4 → FY = calendar year + 1, else FY = calendar year
function monthToFYQuarter(dateStr) {
  const [year, month] = dateStr.split("-").map(Number);
  const q  = month <= 3 ? 4 : month <= 6 ? 1 : month <= 9 ? 2 : 3;
  const fy = month >= 4 ? year + 1 : year;
  return { label: `Q${q} FY${String(fy).slice(2)}`, sortKey: fy * 10 + q };
}

// Derives last 4 distinct quarters from earnHistory for monthly-frequency indices.
// For each quarter, takes the LAST month's earnings value (most recent data point).
// For quarterly-frequency indices, uses the pre-built last4 array unchanged.
function deriveLast4(earnHistory, dataFrequency, existingLast4) {
  if (dataFrequency !== "monthly") return existingLast4 || [];
  if (!earnHistory?.length) return [];

  const quarterMap = new Map();
  earnHistory.forEach(({ d, e }) => {
    const { label, sortKey } = monthToFYQuarter(d);
    // Overwrite — last month in each quarter wins (most recent reading)
    quarterMap.set(label, { d: label, e, sortKey });
  });

  return Array.from(quarterMap.values())
    .sort((a, b) => a.sortKey - b.sortKey)
    .slice(-4)
    .map(({ d, e }) => ({ d, e })); // strip internal sortKey before returning
}

