// ─── DATA HOOK ─────────────────────────────────────────────────────────────────
function useSectorData() {
  const embedded = {
    sectors:     EMBEDDED_DATA.sectors,
    loading:     false,
    error:       null,
    source:      "embedded",
    lastUpdated: EMBEDDED_DATA.generated_at ?? null,
  };

  const [state, setState] = useState(
    REMOTE_DATA_URL
      ? { sectors:null, loading:true, error:null, source:null, lastUpdated:null }
      : embedded
  );
  // FIX 6: tick bumps force useEffect to re-run → real refetch
  const [tick, setTick] = useState(0);

  useEffect(() => {
    if (!REMOTE_DATA_URL) return;
    let cancelled = false;
    setState(s => ({ ...s, loading:true, error:null }));
    // cache-bust every fetch to bypass GitHub raw CDN (5-min cache)
    const url = `${REMOTE_DATA_URL}?t=${Date.now()}`;
    fetch(url)
      .then(r => { if (!r.ok) throw new Error(`HTTP ${r.status}`); return r.json(); })
      .then(data => { if (!cancelled) setState({ sectors:data.sectors, loading:false, error:null, source:"remote", lastUpdated:data.generated_at ?? null }); })
      .catch(err => {
        if (cancelled) return;
        console.warn("[data] remote fetch failed:", err.message, "→ fallback to embedded");
        setState({ ...embedded, error: err.message });
      });
    return () => { cancelled = true; };
  }, [tick]);

  // ─── AUTO-REFRESH: poll every 30 min so 5 PM pipeline data is picked up ───
  useEffect(() => {
    if (!REMOTE_DATA_URL) return;
    const id = setInterval(() => setTick(t => t + 1), 30 * 60 * 1000);
    return () => clearInterval(id);
  }, []);

  return {
    sectors:     state.sectors,
    loading:     state.loading,
    error:       state.error,
    source:      state.source,
    lastUpdated: state.lastUpdated,
    refetch:     () => REMOTE_DATA_URL
      ? setTick(t => t + 1)
      : console.info("[data] refetch skipped — no REMOTE_DATA_URL"),
  };
}

// ─── DATA ADAPTER ─────────────────────────────────────────────────────────────
// SINGLE SOURCE OF TRUTH: all median PE values come from Screener.in
// (scraped daily into medians_curated.json by scrape_medians.py).
// stats.medianPE is kept only as a bound for chart outlier filtering.
function mapSectorData(raw, sectorName) {
  const cur   = raw.current ?? {};
  const stats = raw.stats   ?? {};
  // FIX 2: derive correct last4 from earnHistory for monthly indices
  const fixedLast4 = deriveLast4(raw.earnHistory, raw.dataFrequency, raw.last4);

  // Screener-sourced medians (ground truth)
  const med3  = cur.median3y  ?? null;
  const med5  = cur.median5y  ?? null;
  const med10 = cur.median10y ?? null;
  // Unified "medianPE" shown in KPIs/chart = Screener 10Y (long-run through-cycle).
  // Fallback chain: 10Y → finalMedianPE → 5Y → null.
  const unifiedMedian = med10 ?? cur.finalMedianPE ?? med5 ?? null;

  return {
    curPE:          cur.pe,
    marketPE:       cur.marketPE,
    peSource:       cur.pe_source ?? null,
    curPrice:       cur.price,
    curEarn:        cur.earn,
    isPB:           cur.isPB  ?? false,
    pb:             cur.pb    ?? null,
    // ─── UNIFIED medians (all Screener-sourced) ───
    medianPE:       unifiedMedian,     // was: stats.medianPE (computed from short ts)
    med3:           med3,              // was: stats.med3
    med5:           med5,              // was: stats.med5
    highPE:         stats.highPE,
    lowPE:          stats.lowPE,
    cagr3:          stats.cagr3,
    cagr5:          stats.cagr5,
    earnTrend:      stats.earnTrend,
    median3y:       med3,
    median5y:       med5,
    median10y:      med10,
    finalMedianPE:  cur.finalMedianPE  ?? null,
    upside3y:       cur.upside3y       ?? null,
    upside5y:       cur.upside5y       ?? null,
    valuationGap:   cur.valuationGap   ?? null,
    classification: cur.classification ?? null,
    // ─── Freshness + sanity flags (set by pipeline.py) ───
    dataStale:      cur.dataStale      ?? false,
    mediansSource:  cur.mediansSource  ?? "unknown",   // fresh | stale | missing
    nseMismatch:    cur.nseMismatch    ?? null,        // deviation ratio or null
    // ts-derived chart scale bound (internal use only)
    statsPeCap:     stats.medianPE ?? unifiedMedian,
    ts:             raw.ts             ?? [],
    earn3y:         raw.earnHistory    ?? [],
    last4:          fixedLast4,          // ← FIX 2: correct quarter labels
  };
}

// ─── STALE / MISMATCH BANNER ─────────────────────────────────────────────────
function StaleDataBanner({ data }) {
  if (!data) return null;
  const msgs = [];
  if (data.mediansSource === "stale")
    msgs.push("Screener medians not refreshed today — showing yesterday's values");
  else if (data.mediansSource === "missing")
    msgs.push("No Screener medians available for this sector");
  if (data.nseMismatch != null)
    msgs.push(`NSE live PE vs Screener 10Y median deviates ${(data.nseMismatch * 100).toFixed(0)}% — check data plumbing`);
  if (!msgs.length) return null;
  return (
    <div style={{ background:"#fff7ed", border:"1.5px solid #fdba74", borderRadius:8,
                  padding:"8px 14px", marginBottom:12, fontSize:11, color:"#9a3412",
                  display:"flex", alignItems:"center", gap:8 }}>
      <span style={{ fontSize:14 }}>⚠</span>
      <span>{msgs.join(" · ")}</span>
    </div>
  );
}

