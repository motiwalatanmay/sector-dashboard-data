    const bdr     = isTop3 ? "#dcfce7" : isBottom ? "#fecaca" : "#f3f4f6";
    const leftBdr = isTop3 ? (rank===1?"#16a34a":"#86efac") : isBottom ? "#fca5a5" : "#e5e7eb";
    const rankCol = isTop3 ? "#16a34a" : isBottom ? "#dc2626" : "#9ca3af";
