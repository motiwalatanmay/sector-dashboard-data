function zone(peOrGap, medPE) {
  let gap;
  if (medPE === undefined) { gap = peOrGap; }
  else {
    if (!peOrGap || !medPE || peOrGap <= 0 || medPE <= 0) return "neutral";
    gap = (peOrGap / medPE) - 1;
  }
  if (gap == null || isNaN(gap)) return "neutral";
  return gap < -0.20 ? "cheap" : gap > 0.20 ? "expensive" : "fair";
}

